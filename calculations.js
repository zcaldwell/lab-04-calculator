export function add_two(num){
    return num + 2;
}

export function add(num1, num2) {
    return num1 + num2;
}

export function sub(num1, num2) {
    return num1 - num2;
}

export function times(num1, num2) {
    return num1 * num2;
}

export function div(num1, num2) {
    return num1 / num2;
}

export function mod(num1, num2) {
    return num1 % num2;
}

export function int(num1, num2) {
    return Math.floor(num1 / num2);
}

export function pyth(num1, num2) {
    return Math.sqrt(Math.pow(num1, 2) + Math.pow(num2, 2));
}

export function expo(num1, num2) {
    return Math.pow(num1, num2);
}